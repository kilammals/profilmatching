				<h3>Penilaian Tunjangan Tenaga Harian Lepas dengan Profile Matching (PENA HARIS)</a></h3>
				<p align="justify">
					Profile matching adalah sebuah mekanisme 
					pengambilan keputusan dengan mengasumsikan bahwa 
					terdapat tingkat variabel prediktor yang ideal yang
					harus dipenuhi oleh subyek yang diteliti, bukannya 
					tingkat minimal yang harus dipenuhi atau dilewati. (Kusrini, 2007).
					<br>
					Profile macthing ini dibuat untuk mengukur kinerja Tenaga Harian Lepas (THL)
					pada sebuah instansi di Kabupaten Gresik.
				</p>
				<p>
					<h3>Langkah-langkah:</h3>
					1. Menentukan Aspek penilaian, bobot aspek, bobot Core Factor dan bobot Secondary Factor. Setiap aspek dibagi lagi menjadi beberapa faktor dengan target nilai yang ditetapkan<br>
					2. Menentukan Kandidat<br>
					3. Memasukkan Nilai faktor setiap kandidat<br>
					4. Menghitung GAP<br>
					5. Konversi nilai GAP dengan tabel bobot nilai GAP & menghitung serta mengelompokkan nilai Core Factor dan Secondary Factor pada setiap aspek<br>
					6. Menghitung Nilai total tiap kandidat<br>
					7. Menghitung nilai akhir (Rangking) untuk menentukan peringkat kelayakan.<br>

				</p>
